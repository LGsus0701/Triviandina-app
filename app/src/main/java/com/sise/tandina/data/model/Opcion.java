package com.sise.tandina.data.model;

public class Opcion {

    private int idOpcion;
    private String descripcion;
    private boolean valido;

    public int getIdOpcion() {
        return idOpcion;
    }

    public String getDescripcion() {
        return descripcion;
    }

    public boolean isValido() {
        return valido;
    }
}
